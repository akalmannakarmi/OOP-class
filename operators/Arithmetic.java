package operators;

public class Arithmetic{
    public static void main(String []arg){
        int a = 5;
        int b = 3;
        int sum = a + b;
        System.out.println(sum);
        int difference = a - b;
        System.out.println(difference);
        int product = a * b; 
        System.out.println(product);
        int quotient = a / b; 
        System.out.println(quotient);
        int remainder = a % b;
        System.out.println(remainder);
    }
}
