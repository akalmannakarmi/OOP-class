package operators;

public class Unary {
    public static void main(String []arg){
        int a = 5;
        int b = -a;
        System.out.println(b);
        boolean isTrue = !false;
        System.out.println(isTrue);
        int c = ++a;
        System.out.println(c);
        int d = --b;
        System.out.println(d);
        
    }
}
